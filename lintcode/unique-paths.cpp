#include <iostream>

#include <vector>
using namespace std;

class Solution {
public:
    /**
     * @param n, m: positive integer (1 <= n ,m <= 100)
     * @return an integer
     */
    int uniquePaths(int m, int n) {
        vector<vector<int> > dp(m, vector<int>(n, 1));

        for (int i = 1; i < m; i++) {
            for (int j = 1; j < n; j++) {
                dp[i][j] = dp[i - 1][j] + dp[i][j - 1];
            }
        }

        return dp[m - 1][n - 1];
    }
};

int main(int argc, char* argv[]) {
    int m = 3;
    int n = 3;
    cout<<Solution().uniquePaths(m, n);
}
